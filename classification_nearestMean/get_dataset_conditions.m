function [conditions, condition_ids] = get_dataset_conditions(val_set)
% Get the conditions in each evaluation dataset
%
% Inputs:
%   val_set = string; 'multidose', 'singledose', or 'sleep'
%
% Outputs:
%   conditions = cell array; each cell holds a string labelling a condition
%       in the dataset
%   condition_ids = vector; each value gives each condition an ID
%       IDs which are 0 or smaller are "unconscious"
%       IDs which are 1 or greater are "conscious"

switch val_set
    case 'train'
        conditions = {...
            'Wake',...
            'Anest'};
        condition_ids = [1 0];
    case {'multidose', 'multidose8', 'multidose4', 'multidoseBatchNormalised', 'multidose8BatchNormalised', 'multidose4BatchNormalised'}
        conditions = {...
            'Isoflurane_1.2',...
            'Isoflurane_0.6',...
            'Wake',...
            'Post_Isoflurane',...
            'Recovery'};
        condition_ids = [-1 0 1 2 3];
    case {'singledose', 'singledoseBatchNormalised'}
        conditions = {...
            'Isoflurane',...
            'Wake',...
            'PostIsoflurane',...
            'Recovery'};
        condition_ids = [0 1 2 3];
    case {'sleep', 'sleepBatchNormalised'}
        conditions = {...
            'sleepLate',...
            'sleepEarly',...
            'wake',...
            'wakeEarly'};
        condition_ids = [-1 0 1 2];
end

end

